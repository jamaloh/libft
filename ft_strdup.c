#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int				i;
	unsigned char	*ptr;

	ptr = malloc(ft_strlen(s1) + 1);
	if (!ptr)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		ptr[i] = (unsigned char)s1[i];
		i++;
	}
	ptr[i] = '\0';
	return ((char *)ptr);
}
